package tp.backend.position;

public class Position {
  private int x;
  private int y;

  public Position() {
    this.x = -1;
    this.y = -1;
  }

  public int getX() {
    return this.x;
  }

  public void setX(int x) {
    this.x = x;
  }

  public int getY() {
    return this.y;
  }

  public void setY(int y) {
    this.y = y;
  }
}
